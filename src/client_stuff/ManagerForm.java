    private void jBtnDelGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDelGINActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Действительно удалить?", 
                "Подтверждение", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            if (goodsId != null) {
                try {
                    manager.deleteGIN(new GoodsInInv(goodsId, invId, 0, 0));
                    refreshGIN(invId);
                } catch (RemoteException ex) {
                    Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jBtnDelGINActionPerformed

    private void jBtnDelInvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDelInvActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Действительно удалить?", 
                "Подтверждение", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            if (invId != null) {
                try {
                    manager.deleteInvoice(new Invoice(invId, null, 0));
                    refreshInvoice();
                } catch (RemoteException ex) {
                    Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                        
            }
        }
    }//GEN-LAST:event_jBtnDelInvActionPerformed

