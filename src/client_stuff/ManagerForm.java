    private void refreshInvoice() throws RemoteException{
        InvoiceModel im = new InvoiceModel(manager.getInvoiceList());
        jTableInvoice.setModel(im);
        jTableInvoice.getColumnModel().removeColumn(jTableInvoice.getColumnModel().getColumn(0));
    }
    
    private void refreshGIN(int invId) throws RemoteException{
        GINModel ginm = new GINModel(manager.getGINList(invId));
        jTableGoods.setModel(ginm);
        jTableGoods.getColumnModel().removeColumn(jTableGoods.getColumnModel().getColumn(0));
        jTableGoods.getColumnModel().removeColumn(jTableGoods.getColumnModel().getColumn(0));
    } 
    
